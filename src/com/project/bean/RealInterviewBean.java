package com.project.bean;

import java.util.Date;

public class RealInterviewBean {
	private int id;
	private Date real_date;
	private String real_company;
	private String real_position;
	private String real_result;
	private String real_feedback;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Date getReal_date() {
		return real_date;
	}
	public void setReal_date(Date real_date) {
		this.real_date = real_date;
	}
	public String getReal_company() {
		return real_company;
	}
	public void setReal_company(String real_company) {
		this.real_company = real_company;
	}
	public String getReal_position() {
		return real_position;
	}
	public void setReal_position(String real_position) {
		this.real_position = real_position;
	}
	public String getReal_result() {
		return real_result;
	}
	public void setReal_result(String real_result) {
		this.real_result = real_result;
	}
	public String getReal_feedback() {
		return real_feedback;
	}
	public void setReal_feedback(String real_feedback) {
		this.real_feedback = real_feedback;
	}
	
	
	
}
